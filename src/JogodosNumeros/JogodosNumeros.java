    private void estadoItemMudanca(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_estadoItemMudanca
        // TODO add your handling code here:
        if(contador==10 && evt.getStateChange() != ItemEvent.DESELECTED){
            JOptionPane.showMessageDialog(this, "Voc√™ s√≥ pode selecionar 10 numeros no maximo");
            JToggleButton botao;
            botao = (JToggleButton) evt.getItem();
            botao.setSelected(false);
            return;
        }
        if(evt.getStateChange() == ItemEvent.SELECTED){
            contador++;
        }else if(evt.getStateChange() == ItemEvent.DESELECTED){
            contador--;
        }  
        if(contador == 6){
            jbVerificar.setEnabled(true);
        }
    }//GEN-LAST:event_estadoItemMudanca

    private void jbJogarNovActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbJogarNovActionPerformed
        // TODO add your handling code here:
        JToggleButton verificarBotao;
        int i = 0;
        while(i<totalBotoes){
            if(botoes[i] instanceof JToggleButton){
                verificarBotao = (JToggleButton) botoes[i];
                if(verificarBotao.isSelected()){
                    verificarBotao.setSelected(false);
                    if(verificarBotao.getBackground()==Color.MAGENTA){
                        verificarBotao.setBackground(Color.LIGHT_GRAY);
                    }
                }                
            }
            i++;
        }
        jtaResultado.setText("");
        contadorAposta = cart.numeroAposta;
        jbVerificar.setEnabled(false);
        contador = 0;
    }//GEN-LAST:event_jbJogarNovActionPerformed


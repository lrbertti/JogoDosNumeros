    private void pintarJToggle(){
        JToggleButton verificarBotao;
        int i = 0;
        while(i<totalBotoes){
            if(botoes[i] instanceof JToggleButton){
                verificarBotao = (JToggleButton) botoes[i];
                if(verificarBotao.isSelected() && sorteio.existe(Integer.parseInt(verificarBotao.getActionCommand())) == false){
                    verificarBotao.setBackground(Color.MAGENTA);
                }                
            }
            i++;
        }
    }
    
    private void pegarItensSelecionados(){
        JToggleButton verificarBotao;
        int i = 0;
        while(i<totalBotoes){
            if(botoes[i] instanceof JToggleButton){
                verificarBotao = (JToggleButton) botoes[i];
                if(verificarBotao.isSelected()){
                    cart.addNumeros(Integer.parseInt(verificarBotao.getActionCommand()));       
                }  
            }
            i++;
        }
    }
